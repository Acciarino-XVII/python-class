--1
SELECT COGNOME, NOME, DT_ASSUNZ, 'Per anno' AS TIPO_RICERCA
FROM dipendenti
WHERE EXTRACT(YEAR FROM DT_ASSUNZ) = 2021

UNION ALL

SELECT COGNOME, NOME, DT_ASSUNZ, 'Per mese' AS TIPO_RICERCA
FROM DIPENDENTI
WHERE EXTRACT(MONTH FROM DT_ASSUNZ) = 4

ORDER BY TIPO_RICERCA, COGNOME;



--2

SELECT NOME, COGNOME, 'PRIMA AUMENTO' AS DESCRIZIONE, STIPENDIO
FROM DIPENDENTI
WHERE RUOLO <>'Manager'
AND COD_UFF = 'D21'

UNION ALL

SELECT NOME, COGNOME,'DOPO AUMENTO' AS DESCRIZIONE, STIPENDIO * 1.10
FROM DIPENDENTI
WHERE RUOLO <> 'Manager'
AND COD_UFF = 'D21'
ORDER BY COGNOME, DESCRIZIONE; 




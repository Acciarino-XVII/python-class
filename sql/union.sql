SELECT '1' AS TIPO, COD_UFF, 'UFFICIO' AS  INFO, NOME_UFFICIO AS NOME 
FROM UFFICI
WHERE COD_UFF IN ('A00', 'B01' , 'C01')

UNION ALL

SELECT '2' AS TIPO, COD_UFF,  COD_PROG AS INFO, NOME_PROG AS NOME
FROM PROGETTI
WHERE COD_UFF IN ('A00', 'B01' , 'C01')

UNION ALL

SELECT '3' AS TIPO , COD_UFF, COD_DIP AS INFO , COGNOME AS NOME 
FROM DIPENDENTI
WHERE COD_UFF IN ('A00', 'B01' , 'C01')
ORDER BY INFO ASC;




